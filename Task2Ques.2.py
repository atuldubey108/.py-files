# Program to calculate the sum of numbers from 1 to 50 using a for loop

# Step 1: Initialize sum variable
total_sum = 0

# Step 2: Use for loop to iterate from 1 to 50
for number in range(1, 51):
    total_sum += number  # Add current number to total_sum

# Step 3: Display the final sum
print(f"The sum of numbers from 1 to 50 is: {total_sum}")

  